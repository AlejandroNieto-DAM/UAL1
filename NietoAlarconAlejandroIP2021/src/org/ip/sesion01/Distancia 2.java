package org.ip.sesion01;

public class Distancia {

	public static void main(String[] args) {
	
		System.out.println("La distancia del punto (3, 4) al punto (0, 0) es " + Math.sqrt(Math.pow(3-0, 2) + Math.pow(4-0, 2)));
		System.out.println("La distancia del punto (5, 12) al punto (0, 0) es " + Math.sqrt(Math.pow(5-0, 2) + Math.pow(12-0, 2)));
		System.out.println("La distancia del punto (2, 1) al punto (0, 0) es " + Math.sqrt(Math.pow(2-0, 2) + Math.pow(1-0, 2)));

	}

}
